package com.infy.batch2022.assignment3;

public class CandidateReport {

	
	String candidateId;
	String candidateName;
	Integer marks1;
	Integer marks2;
	Integer marks3;
	Character result;
	String grade;
	
	
	
	
	public String getCandidateId() {
		return candidateId;
	}
	public void setCandidateId(String candidateId) {
		this.candidateId = candidateId;
	}
	public String getCandidateName() {
		return candidateName;
	}
	public void setCandidateName(String candidateName) {
		this.candidateName = candidateName;
	}
	public Integer getMarks1() {
		return marks1;
	}
	public void setMarks1(Integer marks1) {
		this.marks1 = marks1;
	}
	public Integer getMarks2() {
		return marks2;
	}
	public void setMarks2(Integer marks2) {
		this.marks2 = marks2;
	}
	public Integer getMarks3() {
		return marks3;
	}
	public void setMarks3(Integer marks3) {
		this.marks3 = marks3;
	}
	public Character getResult() {
		return result;
	}
	public void setResult(Character result) {
		this.result = result;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	 
	
}
